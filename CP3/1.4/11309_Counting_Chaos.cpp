bool isPalindrome( const string& s ) {
    int intVal = atoi( s.c_str() );
    string trimStr = to_string( intVal );

    for ( int i = 0; i < trimStr.size() / 2; i++ ) {
        if ( trimStr[ i ] != trimStr[ trimStr.size() - 1 - i ] ) {
            return false;
        }
    }
    return true;
}

string nextTime( const string& s ) {
    string rv = "";
    int hour = atoi( s.substr( 0, 2 ).c_str() );
    int minute = atoi( s.substr( 2, 2 ).c_str() );

    if ( minute == 59 ) {
        string hourStr = to_string( hour + 1 );
        if ( hourStr == "24" ) hourStr = "00";
        if ( hourStr.size() == 1 ) {
            rv += "0" + hourStr + "00";
        } else {
            rv += hourStr + "00";
        }
    } else {
        string hourStr = to_string( hour );
        string minuteStr = to_string( minute + 1 );

        if ( hourStr.size() == 1 ) {
            hourStr = "0" + hourStr;
        }

        if ( minuteStr.size() == 1 ) {
            minuteStr = "0" + minuteStr;
        }

        rv += hourStr + minuteStr;
    }
    return rv;
}

int main( int argc, char ** argv ) {

    string line = "";
    int N = 0;
    if ( getline( cin, line ) ) {
        stringstream ss( line );
        ss >> N;
    }

    for ( int n = 0; n < N; n++ ) {
        if ( getline( cin, line ) ) {
            string numStr = "";

            for ( int i = 0; i < line.size(); i++ ) {
                if ( isdigit( line[ i ] ) ) {
                    numStr += line[ i ];
                }
            }

            do {
                numStr = nextTime( numStr );
            } while ( ! isPalindrome( numStr ) );

            cout << numStr.substr( 0, 2 ) << ":" << numStr.substr( 2, 2 ) << endl;
        }

    }

    return 0;
}
